                <h1>About Dadi</h1>
                <p>Dadi D. Pudumjee, the Managing Trustee and Artistic Director of the Ishara Puppet Theatre Trust, is a trailblazer in the realm of modern puppetry in India. Dadi Pudumjee entered the field as a young enthusiast in 1958. Today he is well known and established as a pioneer of modern puppetry in India. He served as the President of the Union Internationale de la Marionnette (UNIMA), the worldwide puppetry organization for 15 years.</p>
                <p>Pudumjee's journey into puppetry began as a hobby in his hometown of Pune, India. He pursued formal education in Visual Communication at the National Institute of Design, Ahmedabad, where he was mentored by Meher Contractor at the Darpana Academy of Performing Arts. His passion for puppetry was further nurtured at the Marionette Theatre Institute in Stockholm under Michael Meschke, and through workshops by Bunraku master Sennosuke Yoshida in Japan.</p>
